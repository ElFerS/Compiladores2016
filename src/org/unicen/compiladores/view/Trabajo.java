    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // Accion : Abrir Archivo
       {                                           
               this.archivo = new Archivo();
        JFileChooser fileChooser = new JFileChooser();
        int seleccion = fileChooser.showOpenDialog(jPanel1);
        if (seleccion == JFileChooser.APPROVE_OPTION){
              // Limpiar todo
            this.limpiarTabla(this.jTable1);
            this.limpiarTabla(this.jTable2);
            this.limpiarTabla(this.jTable3);
            this.limpiarTabla(this.jTable4);
            this.list1.removeAll();
            this.list2.removeAll();
            this.jTextArea1.setText("");
            this.jTextArea2.setText("");
            this.jTextField1.setText("");
            this.jTextField2.setText("");
            this.jTextField3.setText("");
            this.jTextField4.setText("");            
              // Levantar Archivo
                File fichero = fileChooser.getSelectedFile();
            try {                                
                archivo.load(fichero,this.jTextArea1, this.jTextArea2);
                this.ts.limpiar();
            } catch (IOException ex) {
                Logger.getLogger(Trabajo.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }  
        
        
    }//GEN-LAST:event_jMenuItem1ActionPerformed

