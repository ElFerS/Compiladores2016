public void OrdenarTablaSentencias(JTable JTableOrdenar){
    javax.swing.table.DefaultTableModel model = ((javax.swing.table.DefaultTableModel)(JTableOrdenar.getModel()));
      JTable table = JTableOrdenar;
    table.setAutoCreateColumnsFromModel(false);

    Vector<?> data = model.getDataVector();
    Collections.sort(data, new ColumnSorter(0));   
    model.fireTableStructureChanged();
}



public void limpiarTabla(JTable t){
        javax.swing.table.DefaultTableModel modelo = ((javax.swing.table.DefaultTableModel)(t.getModel()));
        while (modelo.getRowCount()>0)
                modelo.removeRow(0);
    }
